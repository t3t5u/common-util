package com.github.t3t5u.common.util;

public class IntegerMutableRectangle extends MutableRectangle<Integer> {
	@Override
	public Integer getTop() {
		return super.getTop();
	}

	@Override
	public void setTop(final Integer top) {
		super.setTop(top);
	}

	@Override
	public Integer getLeft() {
		return super.getLeft();
	}

	@Override
	public void setLeft(final Integer left) {
		super.setLeft(left);
	}

	@Override
	public Integer getRight() {
		return super.getRight();
	}

	@Override
	public void setRight(final Integer right) {
		super.setRight(right);
	}

	@Override
	public Integer getBottom() {
		return super.getBottom();
	}

	@Override
	public void setBottom(final Integer bottom) {
		super.setBottom(bottom);
	}
}
